    private void creat() {
        Integer tempIndex =0;
        tempIndex= jComboBox1.getSelectedIndex();

        switch (tempIndex) {
            case 0:
                dsp.setTENLOAIPH("phong don");
                break;
            case 1:
                dsp.setTENLOAIPH("phong doi");
                break;
            case 2:
                dsp.setTENLOAIPH("phong vip");
                break;
        }

        dsp=DSPDAO.queryDSPbyTLP(dsp.getTENLOAIPH());

        p.setMALOAIPH(dsp.getMALOAIPH());

        tempIndex= jComboBox2.getSelectedIndex();

        switch (tempIndex) {
            case 0:
                p.setTINHTRANG("sudung");
                break;
            case 1:
                p.setTINHTRANG("baotri");
                break;
        }
        dsp.setSONGUOI(MyConvert.parseStringToInt(txtSN.getText()));
        dsp.setDONGIA(MyConvert.parseStringToInt(txtDG.getText()));
        p.setGHICHU(txtGC.getText());

    }

    private void paint() {
        String tempStr = dsp.getTENLOAIPH();
        Integer tempIndex = 0;
        switch (tempStr) {
            case "phong don":
                tempIndex = 0;
                break;
            case "phong doi":
                tempIndex = 1;
                break;
            case "phong vip":
                tempIndex = 2;
                break;
        }
        jComboBox1.setSelectedIndex(tempIndex);
        tempStr = p.getTINHTRANG();
        tempIndex = 1;
        switch (tempStr) {
            case "sudung":
                tempIndex = 0;
                break;
            case "baotri":
                tempIndex = 1;
                break;
        }
        jComboBox2.setSelectedIndex(tempIndex);
        txtSN.setText(MyConvert.parseIntToString(dsp.getSONGUOI()));
        txtDG.setText(MyConvert.parseIntToString(dsp.getDONGIA()));
        txtGC.setText(p.getGHICHU());
    }

