    private void btnDatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDatActionPerformed
        // TODO add your handling code here:
        //CODE trước trong luồng phụ
        if (listDv == null || listDv.isEmpty()) {
            Object[] options = {"Có", "Không"};
            int result = JOptionPane.showOptionDialog(this,
                    "Bạn chưa chọn dịch vụ, bạn có muốn chọn tiếp không?",
                    "Thông báo",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE,
                    null,
                    options,
                    options[1]);

            if (result == JOptionPane.NO_OPTION) dispose();
            return;
        }
        if (dateTuNgay.getDate() == null || dateDenNgay.getDate() == null) {
            Object[] options = {"Có", "Không"};
            int result = JOptionPane.showOptionDialog(this,
                    "Bạn chưa chọn ngày bắt đầu và ngày kết thúc, bạn có muốn chọn tiếp không?",
                    "Thông báo",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE,
                    null,
                    options,
                    options[1]);
            if (result == JOptionPane.NO_OPTION) dispose();
            return;
        }
        String mess = new DatPhongDAO().insertDVintoHD(hoadon, listIsSelected, dateTuNgay.getDate(), dateDenNgay.getDate());
        Object[] options = {"Chấp nhận"};
        int result = JOptionPane.showOptionDialog(this,
                mess,
                "Thông báo",
                JOptionPane.OK_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                options,
                options[0]);
        if (result == JOptionPane.OK_OPTION) {
            if (mess.equals("Thành công")) dispose();
        }
    }//GEN-LAST:event_btnDatActionPerformed

